sample_string = "Python"
first_char = sample_string[0]
print(f"First character: {first_char}")


second_onwards = sample_string[1:]
print(f"From second character onward: {second_onwards}")


upto_fifth = sample_string[:5]
print(f"Up to fifth character: {upto_fifth}")

substring_1_to_4 = sample_string[1:4]
print(f"Substring from index 1 to 4: {substring_1_to_4}")


last_char = sample_string[-1]
print(f"Last character: {last_char}")


alternate_chars = sample_string[1::2]
print(f"Alternate characters from index 1: {alternate_chars}")
